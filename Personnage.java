public class Personnage {
    int PV;
    int force;
    int defense;
    String nom;
    String arme;

    public Personnage(int PV, int force, int defense, String nom, String arme) {
        this.PV = PV;
        this.force = force;
        this.defense = defense;
        this.nom = nom;
        this.arme = arme;
    }


}
